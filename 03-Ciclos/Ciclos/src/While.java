public class While {
        public static void main(String[] args) {
            // Variables
            int i = 5;

            // Countdown from 5 to 0
            while (i > 0) {
                System.out.println(i);
                i--; // Decremetar
            }
        }
}

 class For {
    public static void main(String[] args) {
        //Bucle de 5 al 0
        for (int i = 5; i > 0; i--) {
            System.out.println(i);
        }
    }
}
